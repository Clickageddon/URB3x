tiles = []
deadly_tiles = []

spawn_x = 100
spawn_y = 100


# LECTURE MAP
for y, row in enumerate(level_map):
    for x, tile in enumerate(row):

        world_x = x * TILE_SIZE
        world_y = y * TILE_SIZE

        if tile == "1":
            tiles.append(pygame.Rect(world_x, world_y, TILE_SIZE, TILE_SIZE))

        if tile == "2":
            deadly_tiles.append(pygame.Rect(world_x, world_y, TILE_SIZE, TILE_SIZE))

        if tile == "P":
            spawn_x = world_x
            spawn_y = world_y


player = pygame.Rect(spawn_x, spawn_y, 30, 40)

vel_y = 0
speed = 5
jump_power = -18
gravity = 0.7
on_ground = False

camera_x = 0
camera_y = 0


def respawn():
    global vel_y
    player.x = spawn_x
    player.y = spawn_y
    vel_y = 0


# -------------------
# GAME LOOP
# -------------------

while True:

    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            pygame.quit()
            sys.exit()

    keys = pygame.key.get_pressed()

    dx = 0
    dy = 0

    if keys[pygame.K_q]:
        dx -= speed
    if keys[pygame.K_d]:
        dx += speed

    if keys[pygame.K_SPACE] and on_ground:
        vel_y = jump_power

    vel_y += gravity
    dy += vel_y

    # COLLISION HORIZONTALE
    player.x += dx

    for tile in tiles:
        if player.colliderect(tile):

            if dx > 0:
                player.right = tile.left

            if dx < 0:
                player.left = tile.right

    # COLLISION VERTICALE
    player.y += dy
    on_ground = False

    for tile in tiles:

        if player.colliderect(tile):

            if vel_y > 0:
                player.bottom = tile.top
                vel_y = 0
                on_ground = True

            elif vel_y < 0:
                player.top = tile.bottom
                vel_y = 0

    # TILE MORTELLE
    for tile in deadly_tiles:
        if player.colliderect(tile):
            respawn()

    # CAMERA
    camera_x = player.centerx - WIDTH // 2
    camera_y = player.centery - HEIGHT // 2

    # -------------------
    # DESSIN
    # -------------------

    screen.blit(background, (0, 0))

    # tiles normales
    for tile in tiles:
        screen.blit(
            textures["1"],
            (tile.x - camera_x, tile.y - camera_y)
        )

    # tiles mortelles
    for tile in deadly_tiles:
        screen.blit(
            textures["2"],
            (tile.x - camera_x, tile.y - camera_y)
        )

    # joueur
    screen.blit(
        player_texture,
        (player.x - camera_x, player.y - camera_y)
    )

    pygame.display.update()
    clock.tick(60)
